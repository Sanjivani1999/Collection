public class ShapeOperations {
   public int calculateareaofSquare(int side){
       return side * side;
   }
    public int calculateareaogrectangle(int length ,int  width)
    {
        return length * width;
    }
    public double calculateareaofTriangle(double base , double height)
    {
        return 0.5 * base * height;
    }


}
